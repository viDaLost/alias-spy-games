async function loadJSON(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP –æ—à–∏–±–∫–∞: ${res.status} –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ ${url}`);
  return await res.json();
}

